package loops;

public class ForPractice5 {
    public static void main(String[] args) {
        for(char ch='a' ;ch<='z' ; ch++){

            if (ch=='z'){
                System.out.println(ch);

            }else {System.out.print(ch+ ".");

            }

        }
        System.out.println("============================");
        for(char ch='A' ;ch<='Z' ; ch++){

            if (ch=='Z'){
                System.out.println(ch);

            }else {System.out.print(ch+ ".");

            }

        }

        for(int i=0 ;i<=9 ; i++){

            if (i==9){
                System.out.println(i);
            }else {System.out.print(i+".$.");

            }

        }



    }}

